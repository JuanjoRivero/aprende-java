/*
Ejercicio 1: Estructura `if` Básica

Enunciado: Haz un bloque `if` para verificar si el número es positivo.
Si es positivo, debe imprimir "El número es positivo".
*/
public class Ejercicio1 {
    public static void main(String[] args) {
        int numero = 5;
        if (numero > 0){
            System.out.println("El numero es positivo");
        }
    }
}